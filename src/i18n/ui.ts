const UI_MAP: Record<Locale, UiStrings> = {
  en: UI_EN,
  ku: UI_KU,
  ar: UI_AR,
};

export function getUiStrings(locale: Locale): UiStrings {
  return UI_MAP[locale] ?? UI_EN;
}

type PathValue<T, P extends string> = P extends `${infer K}.${infer Rest}`
  ? K extends keyof T
    ? PathValue<T[K], Rest>
    : never
  : P extends keyof T
    ? T[P]
    : never;

export type TranslationKey = {
  [K in keyof UiStrings]: UiStrings[K] extends string
    ? K
    : UiStrings[K] extends readonly string[]
      ? K
      : {
          [SK in keyof UiStrings[K]]: UiStrings[K][SK] extends string
            ? `${K & string}.${SK & string}`
            : never;
        }[keyof UiStrings[K]];
}[keyof UiStrings];

export function t(locale: Locale, key: string, vars?: Record<string, string | number>): string {
  const strings = getUiStrings(locale);
  const parts = key.split('.');
  let value: unknown = strings;
  for (const part of parts) {
    if (value && typeof value === 'object' && part in value) {
      value = (value as Record<string, unknown>)[part];
    } else {
      return key;
    }
  }
  if (typeof value !== 'string') return key;
  if (!vars) return value;
  return Object.entries(vars).reduce(
    (acc, [k, v]) => acc.replace(`{${k}}`, String(v)),
    value
  );
}
